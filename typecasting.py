# Type casting :

"""
1) Type casting yani int ko string mai convert krna / int ko string mai / foat ko bhi
2) functions of type casting: int(), str(), float()
 """

# example
var1 = 20
var2 = 28
var3 = "20"
var4 = "28"

# var3 our var4 ko add nahi kr sakte ye string hai use add krne ke liye int function ka use kre
print(int(var3) + int(var4))
print(str(var1) + str(var2))
