# in Keyword
list1 = [1, 2, 3, 4, 5]
print(10 in list1)  # 10 is list mai hoga to true wrna false
# You can also add if else conditional on it.
if 10 in list1:
    print("Is in list")
else:
    print("Not in list")

# not in Keyword
list2 = [10, 11, 12, 13, 14 ,15]
print(20 not in list2)  # 20 is list mai nahi yail kay ??  (ANS: Nahi yaiel to true wrna false)